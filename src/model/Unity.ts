/**
 * Defines the unities, which can measure a product or item.
 * @readonly
 * @enum {string}
 * @see {@link https://www.iso.org/standard/30669.html ISO/IEC 80000}
 */
enum Unity {
  piece = 'pz',
  kilogram = 'kg',
  gram = 'gr',
  pound = 'lb',
  ounce = 'oz',
  liter = 'L',
  mililiter = 'mL',
  quart = 'qt',
  gallon = 'gal',
};

export = Unity;
